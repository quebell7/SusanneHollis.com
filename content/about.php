		<h4>About the Showroom</h4>		
	</div>
	<div class="col-xs-12">
		<p>For over 30 years, Susanne Hollis, Inc., a premier antique dealer specializing in pieces from Asia, Europe, and South America in South Pasadena, has provided a tailored shopping experience for Interior Designers and the design minded.</p>
		<p>Housed in a 19,000 sq. ft. showroom and warehouse, antiques span history and style from China’s 17th century Qing dynasty,France’s 19th century, and Scandinavia 20th century . Frequent travels abroad in search of distinctly unique pieces ensure a consistently replenished inventory. In addition to the showroom’s antiques, Susanne Hollis offers custom reproductions of furniture and accessories, along with on-site refinishing capabilities, home staging, and design expertise. </p>
 	</div>
	
	<div class="col-xs-12">
		<h4>About the Owner</h4>		
	</div>
	<div class="col-xs-12">
		<p>Founder and owner, Hollis has more than three decades of expertise with fine antiques. Her innate curiosity about culture and history took root during her upbringing in Denmark where she honed her proficiency of Scandinavian antiquities. While residing in Taiwan, she immersed herself in exploring the region’s abundant antiques representing life and dynasties of centuries past. Most recently, Hollis’s son, Mik joined the family business in his new role as President. He plans to bring the world of antiques into the modern digital age as he believes that pieces with a past continue to have a story worth sharing.</p>		
